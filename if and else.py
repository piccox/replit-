height = int(input("what is your height in cm?"))

#if True or False
# > ; greater than 
if height > 120: 
  print("you can ride the rollercoaster!")
else: 
  print("sorry, you can not ride!")




# == precisely equal to 
if height == 120: 
  print("you can ride the rollercoaster!")
else: 
  print("sorry, you can not ride!")


# height >= ; greater or equal to 
if height >= 120: 
  print("you can ride the rollercoaster!")
else: 
  print("sorry, you can not ride!")







